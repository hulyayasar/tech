package day10dowhileloop;



public class DoWhile02 {

	public static void main(String[] args) {
		
		
		
		System.out.println("Enter an integer");
        
        int i=0;
        do {
        	if(i%2==1) {
        		System.out.println(i);
        	}
        	i++;
        }while(i<=100);
        
        
		
		
	}

}
